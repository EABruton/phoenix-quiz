defmodule Backend.Quizzes do
  @moduledoc """
  The Questions context.
  """

  import Ecto.Query, warn: false
  require Logger
  alias Backend.Repo
  require Logger

  alias Backend.Quizzes.Question

  @results_per_page 10

  @doc """
  Returns the list of questions.

  ## Examples

      iex> list_questions(1)
      [%Question{}, ...]

  """
  def list_questions(page, results_per_page \\ @results_per_page) do
    # page 1 means no offset
    offset = results_per_page * (page - 1)
    limit = offset + results_per_page

    query =
      from(q in Question,
        limit: ^limit,
        offset: ^offset,
        # TODO: Consider getting rid of this, as the answers usually aren't being used
        # left_join: a in assoc(q, :answers),
        # preload: [answers: a],
        order_by: [desc: q.inserted_at]
      )

    questions = Repo.all(query)
    total_count = Repo.aggregate(Question, :count)
    total_pages = trunc(Float.ceil(total_count / results_per_page))

    %{data: questions, total_count: total_count, total_pages: total_pages, current_page: page}
  end

  @doc """
  Gets a single question.

  Raises `Ecto.NoResultsError` if the Question does not exist.

  ## Examples

      iex> get_question!(123)
      %Question{}

      iex> get_question!(456)
      ** (Ecto.NoResultsError)

  """
  def get_question!(id), do: Repo.get!(Question, id)

  @doc """
  Creates a question.

  ## Examples

      iex> create_question(%{field: value})
      {:ok, %Question{}}

      iex> create_question(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_question(attrs) do
    %Question{}
    |> Question.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a question.

  ## Examples

      iex> update_question(question, %{field: new_value})
      {:ok, %Question{}}

      iex> update_question(question, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_question(%Question{} = question, attrs) do
    question
    |> Question.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a question.

  ## Examples

      iex> delete_question(question)
      {:ok, %Question{}}

      iex> delete_question(question)
      {:error, %Ecto.Changeset{}}

  """
  def delete_question(%Question{} = question) do
    Repo.delete(question)
  end

  @doc """
  Deletes a list of questions.

  ## Examples

      iex> delete_questions([%Ecto.UUID{}, ...])
      {:ok, 3}
  """
  @spec delete_questions(question_ids :: [String.t()]) :: {:ok, non_neg_integer()}
  def delete_questions(question_ids) do
    question_count = length(question_ids)

    query = from(question in Question, where: question.id in ^question_ids)

    with {^question_count, _} <- Repo.delete_all(query) do
      {:ok, question_count}
    else
      _ ->
        Logger.warning("Invalid delete questions IDs: #{IO.inspect(question_ids)}")
        {:error, :bad_request}
    end
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking question changes.

  ## Examples

      iex> change_question(question)
      %Ecto.Changeset{data: %Question{}}

  """
  def change_question(%Question{} = question, attrs \\ %{}) do
    Question.changeset(question, attrs)
  end

